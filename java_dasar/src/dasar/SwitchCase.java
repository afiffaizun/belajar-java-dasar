package dasar;

public class SwitchCase {

	public static void main(String[] args) {
		
		String nilai = "A";
		
		switch(nilai) {
		case "A":
			System.out.println("Luar Biasa");
			break;
			
		case "B":
			System.out.println("Biasa");
			break;
			
		default:
			System.out.println("Belajar Lebih Giat");
		}

	}

}
