package dasar;

public class IfElse {

	public static void main(String[] args) {
		
		int nilai = 96;
		int plus = 85;
		
		if ( nilai >= 95 && plus >= 90 ) {
			System.out.println("A");
			
		} else if ( nilai >= 85 && plus >= 80) {
			System.out.println("B");
			
		} else {
			System.out.println("C");
		}

	}

}
