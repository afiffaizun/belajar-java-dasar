package dasar;

public class TipeDataNumber {

	public static void main(String[] args) {
		
		int angkaInteger = 15;
		double angkaDouble= 3.14;
		float angkaFloat = 10.30f;
		long angkaLong = 9000000000000000L;
		
		System.out.println("Integer : " + angkaInteger);
		System.out.println("Double  : " + angkaDouble);
		System.out.println("Float   : " + angkaFloat);
		System.out.println("Long    : " + angkaLong);
		

	}

}
